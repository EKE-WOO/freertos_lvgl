void print_usart1(char *format, ...)
{
		char buf[256];
		va_list ap;                //声明字符指针 ap
		va_start(ap, format);      //初始化 ap 变量
		vsprintf(buf, format, ap); //使用参数列表发送格式化输出到字符串
		HAL_UART_Transmit(&huart1, (uint8_t *)buf, strlen(buf), 100);//100ms内发送
		va_end(ap);
}
