// 修改后的通用波形更新函数
void UpdateWaveform(float new_data[], int data_count, u16* buffer, int* w_index, 
                  int* v_points, u16 y_base, u16 wave_height, u16 color) {
    for(int i=0; i<data_count; i++) {
        u16 y = y_base - (u16)(new_data[i] * wave_height);
        
        // 清除旧点
        if(*v_points >= WAVE_WIDTH) {
            int clear_pos = (*w_index + WAVE_WIDTH) % WAVE_WIDTH;
            LCD_DrawPoint(WAVE_X_START + clear_pos, buffer[clear_pos], BLACK);
        }
        
        // 更新缓冲区
        buffer[*w_index] = y;
        *w_index = (*w_index + 1) % WAVE_WIDTH;
        *v_points = (*v_points < WAVE_WIDTH) ? *v_points + 1 : WAVE_WIDTH;
    }
    
    // 绘制新点
    for(int i=0; i<data_count; i++) {
        int pos = (*w_index - data_count + i + WAVE_WIDTH) % WAVE_WIDTH;
        int x = WAVE_X_START + pos;
        u16 y = buffer[pos];
        
        LCD_DrawPoint(x, y, color);
        
        // 残影消除
        if(pos == *w_index) {
            for(int h=0; h<wave_height; h++) {
                if(h != y) LCD_DrawPoint(x, y_base - h, BLACK);
            }
        }
    }
}
